class Human:
    def __init__(self,name,age,hobby):
        self.name=name
        self.age=age
        self.hobby=hobby

obj1=Human("John",25,"Reading")
obj2=Human("Emma",23,"Cooking")
print(obj1)
print(obj1.name)
print(obj1.age)
print(obj1.hobby)
print(obj2)
print(obj2.name)
print(obj2.age)
print(obj2.hobby)